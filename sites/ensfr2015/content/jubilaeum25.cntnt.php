<h2>Die 100 Mann OK-Party in Louisenlund 2007</h2>
<p>
Ca. 140 der Ok-Jolle verbundenen Menschenkinder fanden den Weg nach Louisenlund,
um bei traumhaftem Wetter und einer sehr leichten Brise spannende Kutter-Wettfahrten auszutragen
sowie Erlebtes aus der jüngeren und älteren OK-Historie aufzufrischen. 
</p>
 

